console.log("====== Operadores Matemáticos")
var operador = 5, 
    operando =10; 

var soma = operador + operando;
console.log(`A soma ${operador} e ${operando} e igual a: ${soma} ` );

var multiplicação = operando * operando;
console.log(`${operador}X${operando}=${multiplicação}`)

var subtração = operador - operando;
console.log(`${operador}-${operando}=${subtração}`)

var divisao = operador / operando;
console.log(`${operador} dividido por ${operando} é igual ${divisao}`);

var potencia = operador ** operando;
console.log(`${operador} elevado a ${operando} é igual ${potencia}`);

var restoDaDivisao = operador % operando;
console.log(`O resto da divisao ${operador}/${operando} é igual ${restoDaDivisao}`)